// Initialize a variable to store the sum
let sum = 0;

// Using a for loop to iterate from 1 to 10
for (let i = 1; i <= 10; i++) {
    sum += i; // Add the current number to the sum
}

// Output the result
console.log("The sum of the first 10 natural numbers is:", sum);


